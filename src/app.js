function getNestedValue(obj, path) {
  return path.split('.').reduce((current, key) => current && current[key], obj) || '';
}

let currentLang = localStorage.getItem('lang') || 'en';
let currentTheme = localStorage.getItem('theme') || 'dark';

const themeToggle = document.querySelector('[data-theme-toggle]');
const langBtns = document.querySelectorAll('[data-lang]');
const i18nElements = document.querySelectorAll('[data-i18n]');

function applyTheme(theme) {
  currentTheme = theme;
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem('theme', theme);
}

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.setAttribute('lang', lang === 'ar' ? 'ar' : lang === 'tr' ? 'tr' : 'en');
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

  langBtns.forEach((btn) => {
    const isActive = btn.getAttribute('data-lang') === lang;
    btn.classList.toggle('active', isActive);
    btn.setAttribute('aria-checked', isActive);
  });

  document.querySelector('title').textContent = getNestedValue(translations[lang], 'meta.title');

  document.querySelector('meta[name="description"]').setAttribute('content', getNestedValue(translations[lang], 'meta.desc'));

  const avatar = document.querySelector('.hero-card-avatar');
  if (avatar) avatar.alt = getNestedValue(translations[lang], 'hero.cardName');

  const copyright = document.querySelector('.footer-copy');
  if (copyright) {
    const year = document.getElementById('year')?.textContent || new Date().getFullYear();
    const name = getNestedValue(translations[lang], 'footer.name');
    const copyText = getNestedValue(translations[lang], 'footer.copy');
    copyright.textContent = `\u00A9 ${year} ${name}. ${copyText}`;
  }

  i18nElements.forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    const value = getNestedValue(translations[lang], key);
    if (value) {
      if (el.tagName === 'META') {
        el.setAttribute('content', value);
      } else {
        el.textContent = value;
      }
    }
  });

  localStorage.setItem('lang', lang);
}

themeToggle?.addEventListener('click', () => {
  applyTheme(currentTheme === 'dark' ? 'light' : 'dark');
});

langBtns.forEach((btn) => {
  btn.addEventListener('click', () => {
    const lang = btn.getAttribute('data-lang');
    if (lang && lang !== currentLang) applyLanguage(lang);
  });
});

applyTheme(currentTheme);
applyLanguage(currentLang);

// ===== HEADER SCROLL =====
const header = document.querySelector('[data-header]');
const topButton = document.querySelector('[data-top]');
const menuBtn = document.querySelector('[data-menu]');
const nav = document.querySelector('[data-nav]');
const navLinks = [...document.querySelectorAll('.nav a')];
const sections = navLinks.map((l) => document.querySelector(l.getAttribute('href'))).filter(Boolean);
const scrollProgress = document.querySelector('.scroll-progress-fill');

function updateHeader() {
  const scrolled = window.scrollY > 24;
  header?.classList.toggle('scrolled', scrolled);
  topButton?.classList.toggle('visible', window.scrollY > 400);

  if (scrollProgress) {
    const total = document.documentElement.scrollHeight - window.innerHeight;
    const pct = total > 0 ? (window.scrollY / total) * 100 : 0;
    scrollProgress.style.width = `${pct}%`;
  }
}

function closeNav() {
  nav?.classList.remove('open');
  menuBtn?.classList.remove('active');
  document.body.classList.remove('menu-open');
}

menuBtn?.addEventListener('click', () => {
  const open = nav?.classList.toggle('open');
  menuBtn.classList.toggle('active', Boolean(open));
  document.body.classList.toggle('menu-open', Boolean(open));
});

navLinks.forEach((l) => l.addEventListener('click', closeNav));
topButton?.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));
window.addEventListener('scroll', updateHeader, { passive: true });
updateHeader();

